#include "fdf.h"

void	ft_clr(char ***str)
{
	size_t		i;
	char		**s;

	s = *str;
	i = 0;
	while (s[i])
	{
		ft_strdel(&s[i]);
		i++;
	}
	free(*str);
	*str = NULL;
}
